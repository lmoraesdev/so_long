#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	char			*dest;
	size_t			len;

	i = 0;
	if (!s)
		return (NULL);
	len = ft_strlen(s) + 1;
	dest = (char *)malloc((sizeof(char) * len));
	if (!dest)
		return (NULL);
	while (s[i])
	{
		dest[i] = f(i, s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
